  `GIFT_ORDER_AMOUNT` int unsigned DEFAULT NULL COMMENT '總金額',
  `GIFT_ORDER_DATETIME` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '下訂時間',
  `GIFT_ORDER_TAKETYPE` tinyint(1) NOT NULL COMMENT '取貨方式',
  `GIFT_ORDER_RECEIVER` varchar(30) DEFAULT NULL COMMENT '收件人',
  `GIFT_ORDER_RECEIVE_ADDRESS` varchar(50) DEFAULT NULL COMMENT '收件地址',
  `GIFT_ORDER_CELLPHONE` char(10) DEFAULT NULL COMMENT '聯絡電話',
  `GIFT_ORDER_STATUS` tinyint(1) DEFAULT NULL COMMENT '訂單狀態',
